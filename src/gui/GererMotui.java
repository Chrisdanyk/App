    private void jButtonMotEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMotEnregistrerActionPerformed
        if (editMode == false) {
            try {
                // TODO add your handling code here:
                System.out.println("hhhhhhhhhhhh");
                if (jTextFieldMotFr.getText().isEmpty() | jTextFieldMotSw.getText().isEmpty() | jTextFieldMotEn.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Tous les champs sont obligatoires!!!");
                } else {
                    MotDao.ajouterMot(jTextFieldMotFr.getText(), jTextFieldMotSw.getText(), jTextFieldMotLi.getText(), jTextFieldMotEn.getText());
                    loadMot();
                    jLabelMessage.setText("Enregistrement effectué avec succès");
                }
            } catch (SQLException ex) {
                Logger.getLogger(GererMotui.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            System.out.println("8888888888888888");
            try {
                // avoir id mot methode
                //
                Mot selectedMot = allMot.get(selectedIndex);
                if (jTextFieldMotFr.getText().isEmpty() | jTextFieldMotSw.getText().isEmpty() | jTextFieldMotEn.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Tous les champs sont obligatoires!!!");
                } else {
                    MotDao.modiferMot(selectedMot.getId(), jTextFieldMotFr.getText(), jTextFieldMotSw.getText(), jTextFieldMotLi.getText(), jTextFieldMotEn.getText());
                    loadMot();
                    jLabelMessage.setText("Modification effectué avec succès");
                }
            } catch (SQLException ex) {
                Logger.getLogger(GererMotui.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        emptyFields();
        deactivateFields();
    }//GEN-LAST:event_jButtonMotEnregistrerActionPerformed

