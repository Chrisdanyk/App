    public void loadMot() throws SQLException {
        model.setNumRows(0);
        allMot = MotDao.allMots();
        for (Mot mot : allMot) {
            model.addRow(new Object[]{mot.getFrancais(), mot.getSwahili(), mot.getLingala(), mot.getAnglais()});

        }
    }

